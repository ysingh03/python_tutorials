
print("############ Arithmetic Operators ################")
# Arithmetic Operators :

print("3 + 2 is ", 3+2)
print("3 - 2 is ", 3-2)
print("3 * 2 is ", 3*2)
print("3 / 2 is ", 3/2)
print("3 // 2 it show integer only is ", 3 // 2)
print("3 ** 2 - 'called as power' is ", 3 ** 2)
print("3 % 2 show remainder only is ", 3 % 2)

print("########### Assignment Operators ##############")
# Assignment Operators :

