print("########### Comparison Operators ##############")

i = 8
print(i == 5)
print(i != 5)
print(i > 5)
print(i < 5)
print(i >= 5)
print(i <= 5)

print("########### Logical Operators ##############")
a = True
b = False

print(a and b)
print(a and a)
print(a or b)
print(b or b)
print(a or a)

print("########### Identity Operators ##############")

print(5 is 5)
print(5 is 8)
print(5 is not 8)
print(5 is not 5)

